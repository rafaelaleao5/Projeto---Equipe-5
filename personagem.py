class Personagem():
    def __init__(self, brasileirao, copadoBrasil, coracao):
        self.brasileirao = brasileirao
        self.copadoBrasil = copadoBrasil
        self.coracao = coracao

    def setbrasileirao(self):
        self.brasileirao += 1

    def getbrasileirao(self):
        return self.brasileirao

    def setCopadoBrasil(self):
        self.copadoBrasil += 1

    def getCopadoBrasil(self):
        return self.copadoBrasil

    def setCoracao(self):
        self.coracao += 1

    def getCoracao(self):
        return self.coracao
        

    

    
        
    
